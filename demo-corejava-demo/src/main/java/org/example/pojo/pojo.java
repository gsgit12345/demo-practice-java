package pojo;

public class pojo {
String em=null;
String lname=null;
public pojo(String em, String lname) {
	super();
	this.em = em;
	this.lname = lname;
}
public String getEm() {
	return em;
}
public void setEm(String em) {
	this.em = em;
}
public String getLname() {
	return lname;
}
public void setLname(String lname) {
	this.lname = lname;
}
}
